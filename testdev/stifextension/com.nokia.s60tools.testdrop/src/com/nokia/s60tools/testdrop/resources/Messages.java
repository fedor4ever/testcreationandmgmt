package com.nokia.s60tools.testdrop.resources;

import java.util.MissingResourceException;
import java.util.ResourceBundle;

/**
 * Class for connecting to text resources needed for constructing messages
 * 
 */
public class Messages {
	private static final String BUNDLE_NAME = "com.nokia.s60tools.testdrop.resources.messages"; 

	private static final ResourceBundle RESOURCE_BUNDLE = ResourceBundle
			.getBundle(BUNDLE_NAME);

	private Messages() {
	}

	public static String getString(String key) {
		try {
			return RESOURCE_BUNDLE.getString(key);
		} catch (MissingResourceException e) {
			return '!' + key + '!';
		}
	}
}
